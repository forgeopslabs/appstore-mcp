//! Subscription offer tools: introductory, promotional, and win-back offers (#8, #10).
//!
//! Schemas verified against Apple's generated OpenAPI models. Promotional and
//! win-back offers carry their per-territory prices as `included`
//! resources linked by `${...}` temporary ids (the format App Store Connect
//! requires for inline creates).

use rmcp::{
    handler::server::wrapper::Parameters, model::*, schemars, tool, tool_router,
    ErrorData as McpError,
};
use serde::Deserialize;
use serde_json::{json, Value};

use super::AppStoreServer;

/// How an offer charges the customer.
#[derive(Debug, Clone, Copy, Deserialize, schemars::JsonSchema)]
#[serde(rename_all = "SCREAMING_SNAKE_CASE")]
pub(crate) enum SubscriptionOfferMode {
    PayAsYouGo,
    PayUpFront,
    FreeTrial,
}

impl SubscriptionOfferMode {
    pub(crate) fn as_api(self) -> &'static str {
        match self {
            SubscriptionOfferMode::PayAsYouGo => "PAY_AS_YOU_GO",
            SubscriptionOfferMode::PayUpFront => "PAY_UP_FRONT",
            SubscriptionOfferMode::FreeTrial => "FREE_TRIAL",
        }
    }
}

/// How long a single offer period lasts.
#[derive(Debug, Clone, Copy, Deserialize, schemars::JsonSchema)]
#[serde(rename_all = "SCREAMING_SNAKE_CASE")]
pub(crate) enum OfferDuration {
    ThreeDays,
    OneWeek,
    TwoWeeks,
    OneMonth,
    TwoMonths,
    ThreeMonths,
    SixMonths,
    OneYear,
}

impl OfferDuration {
    pub(crate) fn as_api(self) -> &'static str {
        match self {
            OfferDuration::ThreeDays => "THREE_DAYS",
            OfferDuration::OneWeek => "ONE_WEEK",
            OfferDuration::TwoWeeks => "TWO_WEEKS",
            OfferDuration::OneMonth => "ONE_MONTH",
            OfferDuration::TwoMonths => "TWO_MONTHS",
            OfferDuration::ThreeMonths => "THREE_MONTHS",
            OfferDuration::SixMonths => "SIX_MONTHS",
            OfferDuration::OneYear => "ONE_YEAR",
        }
    }
}

/// Win-back offer priority.
#[derive(Debug, Clone, Copy, Deserialize, schemars::JsonSchema)]
#[serde(rename_all = "SCREAMING_SNAKE_CASE")]
pub enum WinBackPriority {
    High,
    Normal,
}

impl WinBackPriority {
    fn as_api(self) -> &'static str {
        match self {
            WinBackPriority::High => "HIGH",
            WinBackPriority::Normal => "NORMAL",
        }
    }
}

/// Whether a win-back offer is promoted on the product page.
#[derive(Debug, Clone, Copy, Deserialize, schemars::JsonSchema)]
#[serde(rename_all = "SCREAMING_SNAKE_CASE")]
pub enum WinBackPromotionIntent {
    NotPromoted,
    UseAutoGeneratedAssets,
}

impl WinBackPromotionIntent {
    fn as_api(self) -> &'static str {
        match self {
            WinBackPromotionIntent::NotPromoted => "NOT_PROMOTED",
            WinBackPromotionIntent::UseAutoGeneratedAssets => "USE_AUTO_GENERATED_ASSETS",
        }
    }
}

/// A per-territory price for an offer (territory + price-point pair).
#[derive(Debug, Clone, Deserialize, schemars::JsonSchema)]
pub struct OfferPrice {
    /// Territory ID, e.g. "USA".
    pub territory: String,
    /// The subscriptionPricePoint ID (from list_subscription_price_points).
    pub price_point_id: String,
}

